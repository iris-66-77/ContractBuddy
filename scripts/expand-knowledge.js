async function main() {
  console.log('正在初始化 Embedding 模型...');
  var embeddingService = new EmbeddingService();
  await embeddingService.init();
  console.log('正在加载向量存储...');
  var store = new VectorStore(embeddingService);
  var before = store.getAllDocuments().length;
  console.log('当前知识库条数:', before);

  console.log('正在添加 ' + newLaws.length + ' 条法律知识...');
  for (var i = 0; i < newLaws.length; i++) {
    var law = newLaws[i];
    await store.addDocumentWithEmbedding(law);
    process.stdout.write('\r  进度: ' + (i + 1) + '/' + newLaws.length);
  }
  console.log('');

  var after = store.getAllDocuments().length;
  console.log('扩充完成！');
  console.log('  扩充前:', before + ' 条');
  console.log('  新增:', newLaws.length + ' 条');
  console.log('  扩充后:', after + ' 条');
  console.log('  向量已持久化到 data/vector_store.json');

  // 验证检索
  console.log('\n验证检索:');
  var results = await store.search('押金不退', { limit: 3 });
  console.log('  搜索"押金不退" → mode:', results.mode, ', results:', results.results.length);
  results.results.forEach(function(r) {
    console.log('    [' + r.source + ' ' + r.score + ']', r.content.substring(0, 50));
  });

  var results2 = await store.search('试用期工资低于最低工资', { limit: 3 });
  console.log('  搜索"试用期工资低于最低工资" → mode:', results2.mode, ', results:', results2.results.length);
  results2.results.forEach(function(r) {
    console.log('    [' + r.source + ' ' + r.score + ']', r.content.substring(0, 50));
  });
}

main().catch(function(err) {
  console.error('扩充失败:', err.message);
  process.exit(1);
});
